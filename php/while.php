<?php

//la estructura while se ejecuta mientras la condicion sea verdadera
//si la condicion es falsa, no se ejecuta
//la estructura while es muy parecida a la estructura if
//la diferencia es que if se ejecuta una sola vez y while se ejecuta varias veces
//la estructura while se usa cuando no sabemos cuantas veces se va a ejecutar el codigo

$i = 0;

while ($i < 10) {
    echo "El valor de i es: $i <br>";
    $i++;
}






