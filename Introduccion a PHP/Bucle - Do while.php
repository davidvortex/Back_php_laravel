<?php

$i = 10;

do {
    echo "El valor de i es: $i <br>";
    $i--;
} while ($i >= 0);

// do while se ejecuta al menos una vez, incluso si la condición es falsa
// En este caso, el bucle se ejecuta mientras $i sea mayor o igual a 0.
// Si $i comienza en 10, el bucle se ejecutará 11 veces (de 10 a 0).

